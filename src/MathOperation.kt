fun main(){
    val num1 = readLine()!!.toInt()
    val num2 = readLine()!!.toInt()

    val sum = num1 + num2
    println("Sum: $sum")

    val sub = num1 - num2
    println("Subtract: $sub")

    val multiply = num1 * num2
    println("Multiply: $multiply")

    val mod = num1 % num2
    println("Mod Value: $mod")


}